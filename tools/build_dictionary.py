def main() -> None:
    string_packs = packs()
    meta = {
        "default": "en",
        "languages": [
            {
                "code": L["code"],
                "name": L["name"],
                "native": L["native"],
                "flag": L.get("flag", ""),
                "file": L["code"] + ".json",
            }
            for L in LANGUAGES
        ],
    }
    LANG_DIR.mkdir(parents=True, exist_ok=True)
    (LANG_DIR / "languages.json").write_text(
        json.dumps(meta, ensure_ascii=False, indent=2) + "\n",
        encoding="utf-8",
    )
    for L in LANGUAGES:
        code = L["code"]
        pack = {
            "code": code,
            "name": L["name"],
            "native": L["native"],
            "strings": string_packs[code],
        }
        (LANG_DIR / f"{code}.json").write_text(
            json.dumps(pack, ensure_ascii=False, indent=2) + "\n",
            encoding="utf-8",
        )
    sys.path.insert(0, str(Path(__file__).resolve().parent))
    from split_dictionaries import sync_js_from_json_files

    sync_js_from_json_files()
    print(f"Wrote {len(LANGUAGES)} language files ({len(string_packs['en'])} English keys)")


if __name__ == "__main__":
    main()
